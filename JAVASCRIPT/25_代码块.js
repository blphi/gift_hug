/* 
  我们的程序是由一条条语句构成的
  语句是按照自上至下的书序一条条顺序执行
  在JS中可以使用{} 来为语句进行分组
      同一个{}中的语句我们成为是一组语句
      他们要么么都执行，要么都不执行
      一个{}中的 语句我们也成为较一个代码块 
      在代码块的后边就不用再编写 ; 了
  JS中的代码夸，只具有分组的作用，没有其他用途
      代码块内部的内容，在外部是完全可见的
*/

