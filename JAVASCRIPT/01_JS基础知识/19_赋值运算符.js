/* 
    赋值运算符
        = 可以将符号右侧对的值赋值给符号左侧的变量
        += 
          a +=5  <=> a = a + 5
        -=     
          a -=5 <=> a = a - 5
        *=
          a *=5 <=> a = a*5
        /= 
          a /= 5 <=> a = a/5
        %= 
          a %= 5 <=> a = a % 5     


*/

