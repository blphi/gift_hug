/**
 * 回顾：JS中的数据的基本类型
 * 基本数据类型：string number boolean null undefined 
 * 引用数据类型：object 对象
 * 
 * 
 * - Object 对象
 *    基本数据类型都是单一的值，值和值之间没有任何的联系
 *    对象属于一种复合的数据类型，在对象中可以保存多个不同数据类型的属性
 * - 对象的分类
 *    - 内建对象
 *        由ES标准中定义的对象
 *            在任何的ES的实现中都可以使用
 *            Math String Number  Boolean Function  Object
 *    - 宿主对象
 *        由JS的运行环境提供的对象，目前来讲主要指由浏览器提供的对象
 *        比如：BOM DOM
 *    - 自定义对象
 *        由开发人员自己创建的对象
 * 
 * 
 */